import os
import shutil
from typing import Dict, List

from .metrics import (
    build_table_5_2_row,
    build_table_5_3_rows,
    build_table_5_3_iteration_rows,
    save_fig_5_1_data,
    save_stage_snapshots,
    write_csv,
    write_json,
)


TABLE_5_2_FIELDS = [
    "candidate_viewpoints",
    "final_waypoints",
    "target_coverage_cells",
    "covered_cells",
    "uncovered_cells",
    "coverage_rate",
    "open_path_length_m",
    "closed_path_length_m",
    "mean_segment_length_m",
    "max_segment_length_m",
    "generation_time_sec",
]

TABLE_5_3_FIELDS = [
    "planning_stage",
    "stage",
    "path_length_m",
    "tour_cost",
    "compute_time_sec",
    "relative_improvement",
    "two_opt_swaps",
    "relocate_moves",
]

TABLE_5_3_ITER_FIELDS = [
    "iteration",
    "solver",
    "path_length_m",
    "tour_cost",
    "compute_time_sec",
    "relative_improvement_vs_iter1",
    "feasible_edges",
    "total_edges",
]


def export_plan_run(
    *,
    export_dir: str,
    terrain,
    result,
    cfg,
    yaml_file: str,
    html_file: str,
    total_duration_sec: float,
    export_stages: bool,
) -> Dict[str, str]:
    os.makedirs(export_dir, exist_ok=True)
    raw_dir = os.path.join(export_dir, "raw")
    tables_dir = os.path.join(export_dir, "tables")
    os.makedirs(raw_dir, exist_ok=True)
    os.makedirs(tables_dir, exist_ok=True)

    copied_yaml = os.path.join(export_dir, "waypoints.yaml")
    if yaml_file and os.path.isfile(yaml_file):
        shutil.copy2(yaml_file, copied_yaml)
    copied_html = ""
    if html_file and os.path.isfile(html_file):
        copied_html = os.path.join(export_dir, "coverage_visualization.html")
        shutil.copy2(html_file, copied_html)

    table_5_2 = build_table_5_2_row(
        terrain=terrain,
        viewpoints=result.viewpoints,
        order=result.order,
        cfg=cfg,
        total_duration_sec=total_duration_sec,
    )
    table_5_3 = build_table_5_3_rows(result.stage_records)
    table_5_3_iters = build_table_5_3_iteration_rows(result.iteration_records)

    write_csv(os.path.join(tables_dir, "table_5_2.csv"), [table_5_2], TABLE_5_2_FIELDS)
    write_csv(os.path.join(tables_dir, "table_5_3.csv"), table_5_3, TABLE_5_3_FIELDS)
    write_csv(os.path.join(tables_dir, "table_5_3_iterations.csv"), table_5_3_iters, TABLE_5_3_ITER_FIELDS)
    write_csv(os.path.join(raw_dir, "optimizer_stage_records.csv"), result.stage_records)
    write_csv(os.path.join(raw_dir, "optimizer_iteration_records.csv"), result.iteration_records)

    write_json(
        os.path.join(raw_dir, "summary.json"),
        {
            "solver": result.solver_name,
            "best_cost": float(result.cost),
            "feasible_edges": int(result.feasible_edges),
            "total_edges": int(result.total_edges),
            "waypoint_file": copied_yaml,
            "visualization_file": copied_html,
            "table_5_2": table_5_2,
            "table_5_3": table_5_3,
            "table_5_3_iterations": table_5_3_iters,
        },
    )
    _write_readme(export_dir)

    if export_stages:
        save_stage_snapshots(export_dir, result.stage_snapshots)

    save_fig_5_1_data(export_dir, terrain, result.viewpoints, result.order)
    _save_fig_5_2_data(export_dir, result.stage_records, result.stage_snapshots)
    _save_fig_5_3_placeholder(export_dir, result.viewpoints, result.order)
    return {
        "table_5_2": os.path.join(tables_dir, "table_5_2.csv"),
        "table_5_3": os.path.join(tables_dir, "table_5_3.csv"),
        "summary": os.path.join(raw_dir, "summary.json"),
    }


def _write_readme(export_dir: str) -> None:
    text = """# Chapter 5 Coverage Experiment Output

This directory is generated by `interactive_sip_coverage_planner.py --export-run`.

- `tables/table_5_2.csv`: global coverage path generation statistics.
- `tables/table_5_3.csv`: LKH/2-opt/resampling optimization statistics.
- `tables/table_5_3_iterations.csv`: per-iteration optimization statistics.
- `raw/optimizer_stage_records.csv`: per-stage optimizer records.
- `raw/optimizer_iteration_records.csv`: per-iteration optimizer records.
- `stage_snapshots/*.json`: ordered viewpoint snapshots for reproducible plots.
- `figures/fig_5_1`: terrain, viewpoints, and final tour source data.
- `figures/fig_5_2`: optimization-stage source data.
- `figures/fig_5_3`: global tracking reference source data.
- `waypoints.yaml`: ROS waypoint file used by the online execution stage.
"""
    with open(os.path.join(export_dir, "README.md"), "w") as f:
        f.write(text)


def _save_fig_5_2_data(output_dir: str, stage_records: List[Dict], stage_snapshots: Dict[str, Dict]) -> None:
    fig_dir = os.path.join(output_dir, "figures", "fig_5_2")
    os.makedirs(fig_dir, exist_ok=True)
    write_csv(os.path.join(fig_dir, "optimization_stages.csv"), stage_records)
    for name, snap in stage_snapshots.items():
        rows = []
        for seq, vp in enumerate(snap.get("viewpoints", [])):
            p = vp["position"]
            rows.append(
                {
                    "stage": name,
                    "seq": seq,
                    "x": float(p[0]),
                    "y": float(p[1]),
                    "z": float(p[2]),
                    "viewpoint_index": int(vp["index"]),
                    "face_index": int(vp["face_index"]),
                }
            )
        write_csv(os.path.join(fig_dir, f"{name}_path.csv"), rows)


def _save_fig_5_3_placeholder(output_dir: str, viewpoints, order) -> None:
    fig_dir = os.path.join(output_dir, "figures", "fig_5_3")
    os.makedirs(fig_dir, exist_ok=True)
    rows = []
    for seq, idx in enumerate(order):
        p = viewpoints[idx].position
        rows.append(
            {
                "seq": seq,
                "source": "planned_global_path",
                "x": float(p[0]),
                "y": float(p[1]),
                "z": float(p[2]),
            }
        )
    write_csv(os.path.join(fig_dir, "global_tracking_reference.csv"), rows)
